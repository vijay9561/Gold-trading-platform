              <p> Government of India has identified Bureau of Indian Standard as the sole agency in India to operate the BIS Hallmarking Scheme. The BIS Hallmarking Scheme has been aligned with International criteria on hallmarking (Vienna Convention 1972). A Hallmark, consists of five components i.e. BIS Mark, the Fineness number (corresponding to given cartage), Assaying and Hallmarking Centre's Mark, Jeweller's identification Mark and year of Marking denoted by a code letter and decided by BIS (e.g. code letter `A' was approved by BIS for year 2000, `B' being used for the year 2001 and `C' for 2002 and 'J' for 2008). The marking is done either using punches or laser marking machine. </p>
